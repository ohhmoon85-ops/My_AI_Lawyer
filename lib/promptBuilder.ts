/**
 * 검색된 법령 데이터를 포함한 사용자 메시지 구성
 */
export function buildContextualPrompt(userQuery: string, laws: ParsedLaw[]): string {
  if (laws.length === 0) {
    return `사용자 질문: ${userQuery}

(관련 법령 데이터를 찾지 못했습니다. 일반적인 법률 지식으로 파트 1만 작성해 주시고, 파트 2에는 "관련 법령을 찾을 수 없습니다. 직접 국가법령정보센터(www.law.go.kr)에서 확인하세요."라고 안내해 주세요.)`;
  }

  const lawContexts = laws.map((law) => formatLawForPrompt(law, 15)).join('\n\n---\n\n');

  return `사용자 질문: ${userQuery}

아래는 국가법령정보 API에서 조회한 관련 법령 원문입니다.
파트 1에서는 이 내용을 일반인이 이해하기 쉽게 풀어서 설명하고,
파트 2에서는 아래 원문을 그대로 수록해 주세요.

[관련 법령 원문]
${lawContexts}`;
}

